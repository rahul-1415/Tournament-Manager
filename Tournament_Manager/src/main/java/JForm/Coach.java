    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tname, stat;
        int pc, r, w, l;
        tname = teamname.getText();
        stat = status.getText();
        pc = Integer.valueOf(playercount.getText());
        r = Integer.valueOf(rank.getText());
        w = Integer.valueOf(won.getText());
        l = Integer.valueOf(lost.getText());
        
        Coachc newcoach = new Coachc();
        int a = newcoach.createteam(tname, stat, pc, r, w, l, cid);
        if(a == 0){
        JOptionPane.showMessageDialog(this, " Team Created");
        }
        else {
        JOptionPane.showMessageDialog(this, " Coach has already a team");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

