    public void initial(String login, String password){
        String sql = "Select CoachID from user WHERE Name='"+login+"' And Password='"+password+"'";
        int id1 = 0;
        try {
            Connection connect = DBUtil.getConnection();
            Statement statement = connect.createStatement();
            ResultSet resultSet = statement.executeQuery(sql);

            while (resultSet.next()) {
                id1 = resultSet.getInt("CoachID");
            }
            resultSet.close();
            statement.close();
            connect.close();
           cid = id1;
        } catch (Exception e) {
            e.printStackTrace();
        }        
    }
